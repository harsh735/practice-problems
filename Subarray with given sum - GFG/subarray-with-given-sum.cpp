class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(int arr[], int n, long long s)
    {
        // Your code here
        long long sum = 0;
        int left = 0;
        int right = 0;

        while(right <= n){
            
            if(sum == s)
                return vector<int>{left + 1,right};
                
            else if(sum < s){
                sum += arr[right];
                right++;
            }
            else if(sum > s){
                sum -= arr[left];
                left++;
            }
        }
        return vector<int>{-1};
    }
};

